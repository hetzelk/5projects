class Car():

    def __init__(self, make, model):
        self.make = make
        self.model = model






#this is making an object in the class

make = "Mitsubishi"  
model = "Evolution"

car1 = Car(make, model)

make = "Chevrolet"  
model = "Corvette"

car2 = Car(make, model)

make = "Lambo"  
model = "Veneno"

car3 = Car(make, model)





#this is just representing the information that I pt in that class
#under the name of ___(self).make or (___).model
print(car1.make, car1.model)

print(car2.make, car2.model)

print(car3.make, car3.model)

































